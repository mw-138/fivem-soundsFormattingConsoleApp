static Dictionary<string, HashSet<string>> FormatSoundEntries(string input)
{
    Dictionary<string, HashSet<string>> formattedEntries = new();

    MatchCollection matches = Regex.Matches(input, @"PLAY_SOUND_FRONTEND\([^,]+,\s*""([^""]+)""\s*,\s*""([^""]+)""");

    foreach (Match match in matches.Cast<Match>())
    {
        string sound = match.Groups[1].Value;
        string soundSet = match.Groups[2].Value;

        if (!formattedEntries.TryGetValue(soundSet, out HashSet<string>? value))
        {
            value = [];
            formattedEntries[soundSet] = value;
        }

        value.Add(sound);
    }

    return formattedEntries;
}

static void WriteToFile(string filePath, string content)
{
    try
    {
        using StreamWriter writer = new(filePath);
        writer.Write(content);
    }
    catch (Exception ex)
    {
        Console.WriteLine($"Error writing to file: {ex.Message}");
    }
}

